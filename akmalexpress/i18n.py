UZ_ITEMS = sorted(UZ_TRANSLATIONS.items(), key=lambda item: len(item[0]), reverse=True)


def normalize_language(lang_code):
    if not lang_code:
        return 'ru'
    short_code = str(lang_code).split('-')[0].lower()
    if short_code in SUPPORTED_LANGUAGES:
        return short_code
    return 'ru'


def translate_text(text, lang_code='ru'):
    lang = normalize_language(lang_code)
    text_str = str(text)
    if lang == 'ru':
        return text_str
    return UZ_TRANSLATIONS.get(text_str, text_str)


def translate_html_content(content, lang_code='ru'):
    lang = normalize_language(lang_code)
    if lang == 'ru':
        return content

    translated = content
    for source, target in UZ_ITEMS:
        translated = translated.replace(source, target)

    for pattern, replacer in UZ_REGEX_REPLACEMENTS:
        translated = pattern.sub(replacer, translated)

    return translated
