UZ_ITEMS = sorted(UZ_TRANSLATIONS.items(), key=lambda item: len(item[0]), reverse=True)


def normalize_language(lang_code):
    if not lang_code:
        return 'ru'
    short_code = str(lang_code).split('-')[0].lower()
    if short_code in SUPPORTED_LANGUAGES:
        return short_code
    return 'ru'


def translate_text(text, lang_code='ru'):
    lang = normalize_language(lang_code)
    text_str = str(text)
    if lang == 'ru':
        return text_str
    return UZ_TRANSLATIONS.get(text_str, text_str)


def translate_html_content(content, lang_code='ru'):
    lang = normalize_language(lang_code)
    if lang == 'ru':
        return content

    def translate_fragment(fragment):
        translated_fragment = fragment
        for source, target in UZ_ITEMS:
            translated_fragment = translated_fragment.replace(source, target)
        for pattern, replacer in UZ_REGEX_REPLACEMENTS:
            translated_fragment = pattern.sub(replacer, translated_fragment)
        return translated_fragment

    class UzbekHTMLTranslator(HTMLParser):
        def __init__(self):
            super().__init__(convert_charrefs=False)
            self.parts = []
            self.raw_context = []

        def handle_starttag(self, tag, attrs):
            self.parts.append(self.get_starttag_text() or '')
            if tag in {'script', 'style'}:
                self.raw_context.append(tag)

        def handle_endtag(self, tag):
            self.parts.append(f'</{tag}>')
            if self.raw_context and self.raw_context[-1] == tag:
                self.raw_context.pop()

        def handle_startendtag(self, tag, attrs):
            self.parts.append(self.get_starttag_text() or '')

        def handle_data(self, data):
            if self.raw_context:
                self.parts.append(data)
            else:
                self.parts.append(translate_fragment(data))

        def handle_entityref(self, name):
            self.parts.append(f'&{name};')

        def handle_charref(self, name):
            self.parts.append(f'&#{name};')

        def handle_comment(self, data):
            self.parts.append(f'<!--{data}-->')

        def handle_decl(self, decl):
            self.parts.append(f'<!{decl}>')

        def handle_pi(self, data):
            self.parts.append(f'<?{data}>')

    parser = UzbekHTMLTranslator()
    parser.feed(content)
    parser.close()
    return ''.join(parser.parts)
